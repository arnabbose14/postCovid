#strings or data between the curly inverted comma (single or double) can be connected 
# togather by a process called concatenation using the addition "+" sign.
print("red"+"bull")
# We can concatenate multiple objects
print("south"+"-"+"west")
print("Alexander"+" "+"The"+" "+"Great")
#Observe the " " blank inbetween the inverted commas. Yes use blanks when needed.


# We can also use the term "*" to concatenate strings.
print("aviik"*5) #gives a aviikaviikaviikaviikaviik
#or
print("*"*25)